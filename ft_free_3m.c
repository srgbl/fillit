#include "libft.h"

void		ft_free_3m(char ***s, char **str)
{
	int		e;

	e = 0;
	while (s[e])
		e++;
	while (e)
	{
		ft_freeder(s[e]);
		e--;
	}
	free(s);
	ft_freeder(str);
}
