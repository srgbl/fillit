#include <string.h>

char				*ft_strncpy(char *dst, const char *src, size_t len)
{
	char			*s;

	s = dst;
	while (*src && len--)
		*dst++ = *src++;
	while (!*src && len--)
		*dst++ = '\0';
	return (s);
}
